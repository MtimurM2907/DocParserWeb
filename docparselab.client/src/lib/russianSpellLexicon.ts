/** Частые русские служебные слова — не подсвечивать как орфографические ошибки. */
const KNOWN = new Set(
  [
    'а', 'и', 'но', 'да', 'или', 'ли', 'же', 'бы', 'как', 'что', 'то', 'ни', 'не',
    'в', 'во', 'на', 'по', 'к', 'ко', 'с', 'со', 'от', 'о', 'об', 'обо', 'за', 'из', 'у',
    'до', 'без', 'для', 'при', 'про', 'над', 'под', 'через', 'между', 'перед', 'после',
    'около', 'против', 'если', 'когда', 'пока', 'хотя', 'чтобы', 'потому', 'поэтому',
    'уже', 'еще', 'ещё', 'тоже', 'также', 'только', 'даже', 'вот', 'не', 'ни',
    'я', 'ты', 'он', 'она', 'мы', 'вы', 'они', 'мне', 'ему', 'ей', 'их', 'нам', 'вам',
    'этот', 'эта', 'это', 'эти', 'тот', 'та', 'те', 'так', 'все', 'всё',
    'рф', 'сша', 'ссср', 'оон', 'ес', 'ндс', 'ооо', 'ип',
  ].map((w) => w.toLowerCase()),
);

export function isKnownRussianSpellWord(word: string | undefined | null): boolean {
  if (!word) return false;
  const w = word.trim().toLowerCase();
  if (!w) return false;
  return KNOWN.has(w);
}
